package com.bixin.nft.enums;

public enum TradingRecordState {

    /**
     * 一口价
     */
    ONE_PRICE,

    /**
     * 最高价
     */
    HIGHEST_PRICE,

    /**
     * 报价被超越
     */
    OVER_PRICE,

    /**
     * 报价被接受
     */
    ACCEPT_PRICE,

    /**
     * 出售已取消
     */
    SELL_CANCEL,

    /**
     * 已完成
     */
    DONE;

}
